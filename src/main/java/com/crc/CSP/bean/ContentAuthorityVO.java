package com.crc.CSP.bean;

public class ContentAuthorityVO 
{
	private int id;
	private String admin_id;
	private int tsa;
	private int os;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getAdmin_id() {
		return admin_id;
	}
	
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	
	public int getTsa() {
		return tsa;
	}
	
	public void setTsa(int tsa) {
		this.tsa = tsa;
	}
	
	public int getOs() {
		return os;
	}
	
	public void setOs(int os) {
		this.os = os;
	}
	
}
